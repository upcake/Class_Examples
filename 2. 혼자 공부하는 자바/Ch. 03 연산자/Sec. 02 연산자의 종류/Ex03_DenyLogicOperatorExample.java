package sec02;

public class Ex03_DenyLogicOperatorExample {
	public static void main(String[] args) {
		// 논리 부정 연산자
		boolean play = true;
		System.out.println(play);
		
		play = !play;
		System.out.println(play);
		
		play = !play;
		System.out.println(play);
	} // main()
} // class